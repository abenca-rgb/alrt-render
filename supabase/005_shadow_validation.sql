create or replace view public.shadow_rule_scoreboard as
select
  rule_name as improvement_name,
  shadow_version,
  count(*) as evaluated_alerts,
  count(*) filter (where would_reject) as flagged_alerts,
  count(*) filter (where would_reject and outcome_type in ('SL', 'TIME_EXIT_LOSS')) as losses_avoided,
  count(*) filter (where would_reject and outcome_type in ('TP', 'TP1', 'TP2', 'TP_FULL', 'TIME_EXIT_PROFIT')) as wins_lost,
  avg(move_pct) filter (where outcome_type is not null) as avg_move_pct,
  avg(r_multiple) filter (where r_multiple is not null) as avg_r_multiple,
  case
    when count(*) filter (where would_reject and outcome_type is not null) = 0 then null
    else
      count(*) filter (where would_reject and outcome_type in ('SL', 'TIME_EXIT_LOSS'))::double precision
      / nullif(count(*) filter (where would_reject and outcome_type is not null), 0)
  end as flagged_loss_rate
from public.shadow_rule_results
group by rule_name, shadow_version;

create or replace view public.shadow_combo_scoreboard as
select
  combo_name as improvement_name,
  shadow_version,
  count(*) as evaluated_alerts,
  count(*) filter (where would_reject) as flagged_alerts,
  count(*) filter (where would_reject and outcome_type in ('SL', 'TIME_EXIT_LOSS')) as losses_avoided,
  count(*) filter (where would_reject and outcome_type in ('TP', 'TP1', 'TP2', 'TP_FULL', 'TIME_EXIT_PROFIT')) as wins_lost,
  avg(move_pct) filter (where outcome_type is not null) as avg_move_pct,
  avg(r_multiple) filter (where r_multiple is not null) as avg_r_multiple,
  case
    when count(*) filter (where would_reject and outcome_type is not null) = 0 then null
    else
      count(*) filter (where would_reject and outcome_type in ('SL', 'TIME_EXIT_LOSS'))::double precision
      / nullif(count(*) filter (where would_reject and outcome_type is not null), 0)
  end as flagged_loss_rate
from public.shadow_combo_results
group by combo_name, shadow_version;
