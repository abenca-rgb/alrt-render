create table if not exists public.shadow_evaluations (
  id bigint generated by default as identity primary key,
  candidate_key text not null,
  alert_id text,
  ref_id text,
  symbol text,
  direction text,
  timeframe text,
  setup_type text,
  live_decision text not null default 'ACCEPTED',
  shadow_version text not null,
  event_time_utc timestamptz,
  evaluated_at_utc timestamptz not null default now(),
  outcome_type text,
  outcome_time_utc timestamptz,
  move_pct double precision,
  r_multiple double precision,
  raw_context jsonb,
  unique(candidate_key, shadow_version)
);

create table if not exists public.shadow_rule_results (
  id bigint generated by default as identity primary key,
  candidate_key text not null,
  alert_id text,
  ref_id text,
  rule_name text not null,
  rule_status text not null,
  score_adjustment integer not null default 0,
  would_reject boolean not null default false,
  reason text,
  details jsonb,
  shadow_version text not null,
  evaluated_at_utc timestamptz not null default now(),
  outcome_type text,
  outcome_time_utc timestamptz,
  move_pct double precision,
  r_multiple double precision,
  rejection_would_help boolean,
  rejection_would_hurt boolean,
  unique(candidate_key, shadow_version, rule_name)
);

create table if not exists public.shadow_combo_results (
  id bigint generated by default as identity primary key,
  candidate_key text not null,
  alert_id text,
  ref_id text,
  combo_name text not null,
  rule_names text[] not null,
  combo_status text not null,
  total_score_adjustment integer not null default 0,
  would_reject boolean not null default false,
  reasons text[],
  details jsonb,
  shadow_version text not null,
  evaluated_at_utc timestamptz not null default now(),
  outcome_type text,
  outcome_time_utc timestamptz,
  move_pct double precision,
  r_multiple double precision,
  rejection_would_help boolean,
  rejection_would_hurt boolean,
  unique(candidate_key, shadow_version, combo_name)
);

create table if not exists public.shadow_optimizer_reports (
  id bigint generated by default as identity primary key,
  report_key text not null unique,
